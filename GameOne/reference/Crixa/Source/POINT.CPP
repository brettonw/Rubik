void	Point_2d::operator += (const Vector_2d &v)																//	add a vector to this point
{																																								//	begin
	xy[X] += v[X];																																//	add the x component of the vector
	xy[Y] += v[Y];																																//	add the y component of the vector
}																																								//	end

